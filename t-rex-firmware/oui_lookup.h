// ── Lookup function ───────────────────────────────────────────────────────────

inline OuiInfo ouiLookup(const uint8_t* mac) {
    if (!mac) return {nullptr, nullptr};

    // Locally administered bit — always a spoofed / randomized / virtual MAC
    if (mac[0] & 0x02) return {"LA-MAC", "RandMAC"};

    for (uint16_t i = 0; i < sizeof(OUI_TABLE)/sizeof(OUI_TABLE[0]); i++) {
        if (mac[0] == OUI_TABLE[i].p0 &&
            mac[1] == OUI_TABLE[i].p1 &&
            mac[2] == OUI_TABLE[i].p2)
            return {OUI_TABLE[i].vendor, OUI_TABLE[i].type};
    }
    return {nullptr, nullptr};
}

// Backward-compatible helper — returns just the vendor string (or nullptr).
// Drop-in replacement for the old lookupOui() in wguard.cpp.
inline const char* ouiVendor(const uint8_t* mac) {
    return ouiLookup(mac).vendor;
}
